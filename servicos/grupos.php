			<h1>Grupos</h1>
			<div class="entry">
				<p>A importância do conhecimento e utilização da Psicologia Grupal decorre justamente do fato de que todo indivíduo passa a maior parte do tempo de sua vida convivendo e interagindo com distintos grupos.</p>
				<p>Assim, desde o primeiro grupo natural que existe em todas as culturas - a família nuclear - onde o bebê convive com os pais, avós, irmãos, babás, etc. e, a seguir, passando por creches, escolas maternais e bancos escolares, além de inúmeros grupos de formação espontânea e os costumeiros cursinhos paralelos, a criança estabelece vínculos grupais diversificados.</p>
				<p>Tais grupamentos vão se ampliando e renovando na vida adulta, com a constituição de novas famílias e de grupos associativos, profissionais, esportivos, sociais, etc.<small>(Fonte: ZIMERMAN, David. A importância dos grupos na saúde, cultura e diversidade. Vínculo,  São Paulo ,  v. 4, n. 4, dez.  2007 )</small></p>
				<p>Os atendimentos grupais acontecem semanalmente, com duração de 1h30 minutos onde os participantes possuem um objetivo em comum.</p>
