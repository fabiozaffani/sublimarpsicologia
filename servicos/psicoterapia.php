			<h1>Psicoterapia</h1>
			<div class="entry">
				<p>Conhecida também como terapia, atendimento psicológico ou processo terapêutico, a psicoterapia é uma das práticas da Psicologia. Seus objetivos principais estão em torno do bem estar e da saúde mental dos pacientes.</p>
				<p>Conforme o contrato realizado entre pacientes e psicólogos, os encontros podem ser: semanais, quinzenais ou mais vezes na mesma semana, com a variação do tempo entre 45m a 60m. Tudo irá depender da proposta terapêutica e das necessidades de cada um.</p>
				<p>O paciente desfruta deste tempo para que, através da fala e da escuta afetiva, compreenda seu funcionamento, seus conflitos, entenda comportamentos, e com isso passe a controlar suas atitudes com base no autoconhecimento, autoestima e amor-próprio. </p>
				<p>O paciente poderá experienciar seus diferentes sentimentos e compreender melhor o seu funcionamento psíquico. Através do vínculo entre psicólogo e paciente, ambos vão construindo uma relação terapêutica onde o paciente poderá compreender seus sentimentos, atitudes, comportamentos e sofrimentos. Conforme acontece o autoconhecimento, o paciente se torna mais tolerante em relação aos seus estados psíquicos.</p>
